    private int getPriority(String Priority) {

        int index = 0;

        switch (Priority.toUpperCase()) {
            case "P1":
                index = 1;
                break;
            case "P2":
                index = 2;
                break;
            case "P3":
                index = 3;
                break;
            default:
                index = 4;
                break;
        }

        return index;

    }

    private String setTimerText(JTextField a, JTextField b) {
        try {
            if (a.getText() != null && b.getText() != null) {
                int aI = Integer.parseInt(a.getText());
                int bI = Integer.parseInt(b.getText());

                if (aI >= 0 && bI >= 0) {
                    // Convert hours to minutes and add to minutes
                    int totalMinutes = aI * 60 + bI;

                    // Limit minutes to 60
                    int minutes = totalMinutes % 60;

                    return String.format("%02d:%02d", aI, minutes);
                } else if (aI >= 0) {
                    return String.format("%02d:00", aI);
                } else if (bI >= 0) {
                    return String.format("00:%02d", bI);
                } else {
                    return "00:00";
                }
            } else {
                return "00:00";
            }
        } catch (NumberFormatException e) {
            // Handle the case where the input is not a valid integer
            JOptionPane.showMessageDialog(rootPane, "Numbers Only Please");
            return "00:00";
        }

    }

    public void playSound(String pathName) throws UnsupportedAudioFileException, LineUnavailableException, IOException {

        File file = new File(pathName);
        AudioInputStream audioStream = AudioSystem.getAudioInputStream(file);
        Clip clip = AudioSystem.getClip();
        clip.open(audioStream);

        clip.start();

    }

