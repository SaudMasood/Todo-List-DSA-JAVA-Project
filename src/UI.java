    public void retrieveCompletedTasksFromFile(String filename) {


        reportWindow.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        reportWindow.setSize(420, 420);
        reportWindow.getContentPane().setBackground(new Color(54, 48, 98));
        reportWindow.setLayout(new MigLayout());
        try (BufferedReader reader = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = reader.readLine()) != null) {
                // Assume each line in the file is a task or data to display
                JLabel taskLabel = new JLabel(line);
                taskLabel.setFont(new Font("Times New Roman", Font.PLAIN, 20));
                taskLabel.setForeground(new Color(255, 255, 255));
                reportWindow.add(taskLabel, "wrap");
            }
        } catch (IOException e) {
            // Handle the exception, e.g., show an error message
            JLabel taskLabel = new JLabel("No Task");
            taskLabel.setBounds(0, 0, 100, 100);
            taskLabel.setFont(new Font("Times New Roman", Font.PLAIN, 20));
            taskLabel.setForeground(new Color(255, 255, 255));
            reportWindow.add(taskLabel, "wrap");
        }
        reportWindow.setVisible(true);

    }
    private void AddingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddingButtonActionPerformed
        // TODO add your handling code here:
        AddingArea.setLayout(new MigLayout("wrap"));
        AddingArea.setPreferredSize(AddingArea.getSize());

        if (!firstTime) {
            String Priority = PrioritySetter.getSelectedItem().toString();

            String MSSG = TaskInputer.getText() + " " + Priority;
            int priorityint = getPriority(Priority);

            ch = new Checkbox(MSSG);

            ch.setFont(new Font("Times New Roman", Font.PLAIN, 15));
            ch.setSize(new Dimension(30, 30));

            ch.setForeground(getPriorityColor(priorityint));
            taskManager.AddTask(MSSG, priorityint, ch);
            AddingArea.add(ch, "");

            AddingArea.validate();
            AddingArea.repaint();
            AddingArea.getParent().validate();
            AddingArea.getParent().repaint();
            firstTime = true;
            updatePomodoroUI(Task);
            updatePriorityUI();

            taskManager.saveTasksToFile("tasks.txt");
        } else {
            String Priority = PrioritySetter.getSelectedItem().toString();

            String MSSG = TaskInputer.getText() + " " + Priority;
            int priorityInt = getPriority(Priority);
            ch = new Checkbox(MSSG);

            ch.setForeground(getPriorityColor(priorityInt));

            ch.setFont(new Font("Times New Roman", Font.PLAIN, 15));
            ch.setSize(new Dimension(30, 30));

            taskManager.AddTask(MSSG, priorityInt, ch);
            AddingArea.add(ch, "");
            updatePriorityUI();
            updatePomodoroUI(Task);
            taskManager.saveTasksToFile("tasks.txt");

        }


    }//GEN-LAST:event_AddingButtonActionPerformed

    private void updatePriorityUI() {
        AddingArea.removeAll();

        Task currentTask = taskManager.getFrontTask();
        while (currentTask != null) {
            Color taskColor = getPriorityColor(currentTask.priority); // Get color based on priority
            currentTask.check.setForeground(taskColor);
            AddingArea.add(currentTask.check, "wrap"); // Add the checkbox to the UI
            currentTask = currentTask.next; // Move to the next task in the linked list
        }

        AddingArea.revalidate();
        AddingArea.repaint();
    }

    private Color getPriorityColor(int priority) {
        switch (priority) {
            case 1:
                return new Color(200, 14, 33);
            case 2:
                return new Color(227, 101, 29);
            case 3:
                return new Color(229, 210, 131);
            case 4:
                return new Color(190, 215, 84);
            default:
                return Color.BLACK;
        }
    }
